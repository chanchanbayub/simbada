<script>
    $(document).ready(function() {
        $('#dataTable').DataTable();
    });

    $("#tambah_role_management").submit(function(e) {
        e.preventDefault();

        let role_management = $("#role_management").val();

        $.ajax({
            url: 'role_management/insert',
            method: 'post',
            dataType: 'json',
            data: {
                role_management: role_management,
            },
            beforeSend: function() {
                $(".save").html('<i class="fa fa-spinner"> </i>')
            },
            success: function(response) {
                $(".save").html('<i class="fa fa-paper-plane"></i> Kirim')
                if (response.error) {
                    if (response.error.role_management) {
                        $("#role_management").addClass('is-invalid');
                        $("#error-role").html(response.error.role_management);
                    } else {
                        $("#role_management").addClass('is-invalid');
                        $("#error-role").html('');
                    }
                } else {
                    Swal.fire({
                        icon: 'success',
                        title: `${response.success}`,
                    });
                    setTimeout(function() {
                        location.reload();
                    }, 1000)
                }
            }
        });
    })

    $(".hapus").click(function(e) {
        e.preventDefault();
        let id = $(this).data('id');
        // console.log(id);
        $.ajax({

            url: 'role_management/edit',
            method: 'get',
            dataType: 'json',
            data: {
                id: id
            },
            success: function(response) {
                $("#id_hapus").val(response.id);
            }
        });
    });

    $("#delete").click(function(e) {
        e.preventDefault();
        let id = $("#id_hapus").val();
        // console.log(id);
        $.ajax({

            url: 'role_management/hapus',
            method: 'post',
            dataType: 'json',
            data: {
                id: id
            },
            beforeSend: function() {
                $("#delete").html('<i class="fa fa-spinner"> </i>')
            },
            success: function(response) {
                Swal.fire({
                    icon: 'success',
                    title: `${response.success}`,
                });
                setTimeout(function() {
                    location.reload();
                }, 1000)
            }
        });
    });

    $(".edit").click(function(e) {
        e.preventDefault();
        let id = $(this).data('id');
        // console.log(id);
        $.ajax({

            url: 'role_management/edit',
            method: 'get',
            dataType: 'json',
            data: {
                id: id
            },
            success: function(response) {
                $("#id").val(response.id);
                $("#role_management_edit").val(response.role_management);
            }
        });
    });

    $("#edit_role_management").submit(function(e) {

        e.preventDefault();

        let id = $("#id").val();
        let role_management = $("#role_management_edit").val();

        $.ajax({
            url: 'role_management/update',
            method: 'post',
            dataType: 'json',
            data: {
                id: id,
                role_management: role_management,
            },
            beforeSend: function() {
                $(".update").html('<i class="fa fa-spinner"> </i>')
            },
            success: function(response) {
                $(".update").html('<i class="fa fa-paper-plane"></i> Kirim')
                if (response.error) {
                    if (response.error.role_management) {
                        $("#role_management_edit").addClass('is-invalid');
                        $("#error-role-edit").html(response.error.role_management);
                    } else {
                        $("#role_management_edit").addClass('is-invalid');
                        $("#error-role-edit").html('');
                    }

                } else {
                    Swal.fire({
                        icon: 'success',
                        title: `${response.success}`,
                    });
                    setTimeout(function() {
                        location.reload();
                    }, 1000)
                }
            }
        });
    })
</script>
